# Pehla hissa: Stars barh rahe hain (1 se 4)
for i in range(1, 5):
    for j in range(i):
        print("*", end=" ")
    print()  # Line change karne ke liye

# Doosra hissa: Stars kam ho rahe hain (3 se 1)
for i in range(3, 0, -1):
    for j in range(i):
        print("*", end=" ")
    print()  # Line change karne ke liye